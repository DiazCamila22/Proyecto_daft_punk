    <br>(1987-1993)<br/>
    <p align="center">
        <br>(1987-1993)<br/>
            Thomas Bangalter y Guy-Manuel de Homem-Christo se conocieron en una escuela preparatoria de<br/>
            segundo grado francés de estudios situada en el séptimo distrito de París. Los dos se hicieron buenos<br/>
            amigos y decidieron en 1992, iniciar una banda basada en la guitarra, llamada Darlin' en compañía de<br/>
            Laurent Brancowitz. Bangalter y Homem-Christo tocaban bajo y guitarra, respectivamente, y Brancowitz<br/>
            tocaba los tambores<br/>
    </p>

    <p align="center">
        <br>(1993-1997)<br/>
            Daft Punk acudió a una rave en el Centro Pompidou, en donde conocieron a Stuart Macmillan, integrante<br/>
            de la banda "Slam" y cofundador de la discográfica Soma Records Quality Recordings<br/>
    </p>
    <p align="center">
        Daft Punk también produjo una serie de vídeos musicales dirigidos por Spike Jonze, Michel Gondry,<br/>
        Roman Coppola y Seb Janiak. La colección de videos fue lanzada en 1999 titulada D.A.F.T<br/>
    </p>

    <p align="center">
        <br>(1999-2003)<br/>
    En 2001, el álbum Discovery fue lanzado, orientado en un estilo synthpop, lo que inicialmente sorprendió<br/>
    a muchos fanes de Homework. El grupo comentó que querían reconectar las actitudes que tenían desde<br/>
    niños. Esto los llevó a usar muestras de canciones de los 70 y los 80.<br/>
    </p>
    <p align="center">
        El álbum Daft Club fue lanzado para promocionar el filme. Contiene mezclas del álbum Discovery, este<br/>
        fue solo lanzado por internet por un tiempo limitado.<br/>
    </p>

    <p align="center">
        <br>(2004-2008)<br/>
        Daft Punk dedicó seis semanas para la creación de un nuevo material. El dúo lanzó el álbum "Human<br/>
        After All" en marzo de 2005. Las críticas fueron mixtas, sobre todo citando a su repetitivo carácter y<br/>
        aparentemente por la corta dedicación al grabado del álbum; Daft Punk empezó con su primera<br/>
        colaboración en 2007 con Kanye West haciendo como uno de los sencillos del álbum "Graduation", la<br/>
        cual la canción se llama Stronger, después ganaron un premio Grammy en 2008, haciendo una aparición<br/>
        en el escenario de los Grammys con el cantante West en una pirámide del Alive 2007. Más tarde Daft<br/>
        Punk se empezó a conocer en Estados Unidos de América, Canadá y poco en México.<br/>
    </p>

    <p align="center">
        <br>(2008-2011)<br/>
        En el 2008, Daft Punk fue ubicado en la posición 38 en una encuesta mundial realizada por la revista DJ<br/>
        Mag, después de que, un año antes fueron ubicados en la posición 71. El 8 de febrero de 2009, Daft Punk<br/>
        ganó dos Grammy por su álbum Alive 2007 y su sencillo «Harder, Better, Faster, Stronger»<br/>
        En 2010, Daft Punk fue admitido en la Orden de las Artes y las Letras, una orden de Francia. Bangalter y<br/>
        de Homem-Christo fueron premiados individualmente con el rango Chevalier (caballero).<br/>
    </p>
    <p align="center">
    Soma Records anunció que lanzó una pista anteriormente sin publicar de Daft Punk de sus sesiones de<br/>
    Homework titulada «Drive». La pista fue incluida en la compilación del 20° aniversario de Soma.<br/>
    </p>

    <p align="center">
        <br>(2012-2014)<br/>
        Daft Punk comenzó a trabajar en su cuarto álbum de estudio, con la colaboración del cantante y<br/>
        compositor Pharrell Williams y con contribuciones con el líder de Chic, Nile Rodgers. Desde el 23 de<br/>
        marzo, en iTunes Store se reveló el nombre de este nuevo álbum de estudio por fin llamado Random<br/>
        Access Memories, iniciando los pedidos anticipados, afirmando que el lanzamiento tendría lugar el 21 de<br/>
        mayo. El 19 de abril del mismo año, se da a conocer el primer sencillo del mismo, "Get Lucky", con la<br/>
        participación de Pharrell Williams como voz principal de la canción y Nile Rodgers como el guitarrista.<br/>
    </p>
    <p align="center">
    SEl 7 de octubre de 2014, salió en Youtube el vídeo «Gust of Wind», del cantante Pharrell Williams con la<br/>
    colaboración de Daft Punk, aunque en el vídeo solo se pueden apreciar sus cascos y, al final, una.<br/>
    animación de su silueta.<br/>
    </p>

    <p align="center">
        <br>(2016-2021)<br/>
        En septiembre de 2016, The Weeknd lanzó su exitosa colaboración titulada: "Starboy" junto a Daft Punk.<br/>
        La canción alcanzó el número 1 en la lista estadounidense: Billboard Hot 100. El 17 de noviembre de ese<br/>
        mismo año, The Weeknd sacó otra colaboración con el dúo, titulada: "I Feel It Coming". Ambas<br/>
        pertenecen al álbum de Starboy<br/>
    </p>

    <p align="center">
        <br>(2021-2022)<br/>
        El 22 de febrero de 2021, el dúo anunció su sseparación con un video publicado en todas sus redes<br/>
        sociales titulado ‘Epilogue’ (epílogo) en donde se los muestra a ambos caminando por un desierto; el 22<br/>
        de febrero de 2022, un año después de su disolución, Daft Punk anunció la edición del 25 aniversario des<br/>
        Homework junto con una reedición de Alive 1997. También transmitieron en Twitch una grabación de<br/>
        video de su actuación en el Mayan Theatre de Los Ángeles de su Daftendirektour de 1997<br/>
    </p>

